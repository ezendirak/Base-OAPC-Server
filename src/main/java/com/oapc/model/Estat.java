package com.oapc.model;

import java.util.*;


public class Estat {
	
	
    private String nom;

	private String valor;
	
	
	public Estat() {
		
	}


	public String getNom() {
		return nom;
	}


	public void setNom(String nom) {
		this.nom = nom;
	}


	public String getValor() {
		return valor;
	}


	public void setValor(String valor) {
		this.valor = valor;
	}

	        
}